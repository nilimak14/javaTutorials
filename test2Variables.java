// This program shows all different type of primitive variables in Java


public class test2Variables {

	public static void main(String[] args) {
		// define all variable types in Java
			
			int myNumber; // int is 32 bit integer
			short myshort; // short is 16 bit integer
			long myLong; //long  is 64bit integer
			double myDouble;
			float myFloat; 
			char myChar; //uses single character
			byte myByte; // uses 8 bit value
			boolean myBoolean;
			
			// initiate/declare  all variable types in Java
			
			myNumber = 14;
			myshort = 234;
			myLong = 1234;
			myDouble = 2.345;
			myFloat = 2.456f;
			myChar = 'N';
			myByte = 123;
			myBoolean = false;
			
			// Define and initiate/declare  variables in one go
			
			int myInt = 45;
			
			//printing all variable at console
			
			System.out.println(myNumber);
			System.out.println(myshort);
			System.out.println(myLong);
			System.out.println(myDouble);
			System.out.println(myFloat);
			System.out.println(myChar);
			System.out.println(myByte);
			System.out.println(myBoolean);
			System.out.println(myInt);
			

		}

	}
