//This program shows how String is used in Java in different ways

public class test3Strings {

	public static void main(String[] args) {
		
		int myInt = 8;
		
		String str1 = "Hello";
		String str2 = "Jack";
		String blank = " ";
		String greeting = str1 + blank + str2;
		
		System.out.println(greeting);
		System.out.println("Hello" + " " + "Jack");
		System.out.println("My int is: " + myInt);
		
		double myDouble = 8.7;
		
		System.out.println("My Number is: " + myDouble + ".");

	}

}
