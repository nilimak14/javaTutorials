// This program explains how to use if loop

public class test6IfLoop {

	public static void main(String[] args) {
		int myInt = 15;
				
		// simple if...else loop
		if( myInt < 10 ) {
			System.out.println("Yes, Its True");
		}
		else {
			System.out.println("No, Its False");
		}
			
	    // if..elseif ...else loop
		int myInt1 = 20;
		
		if( myInt1 < 10 ) {
			System.out.println("Yes, Its True");
		}
		else if( myInt1 >20) {
			System.out.println("No, Its False");
		}
		else {
			System.out.println("None  of the above");
		}

	}

}
